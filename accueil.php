              <h3>INFORMATIQUE DE GESTION</h3>
              <p>Qu'est ce que l'informatique de gestion.L’informatique de gestion correspond généralement à de nombreuses tâches effectuées dans le cadre d’une entreprise. Le but est en effet d’organiser des informations, de les vérifier, de les collecter, de manière à être plus productif. L’informatique a beaucoup apporté à la gestion des données au sens large, en faisant gagner un temps précieux, notamment aux professionnels qui aujourd’hui ne pourraient tout simplement plus se passer de ce type de dispositif. Autrefois, ces tâches étaient effectuées à la main, ce qui nécessitait un délai particulièrement important.</p>

              <h3>Les bases de données</h3>
              <p>En premier lieu, on procède à l’hébergement des informations, généralement par le biais de bases de données. Ces « databases » permettent ainsi de stocker les informations. Celles-ci vont ensuite être gérées par différents systèmes de gestion de base de données, de manière à ce que celles-ci soient non seulement bien organisées, mais aussi accessibles facilement. Tout part ainsi de cette base : le stockage d’informations, puis leur gestion par le biais de programmes informatiques conçus pour cela.

              <h3>Objectifs de la formation</h3>
                    <p>Ce programme forme des étudiants sur l'informatique avec des compétences en gestion. Il permet d'obtenir des connaissances sur des technologies et outils en rapport avec la gestion de données et processus, c'est à dire la collecte, la vérification et l'organisation de grands quantités d'inforamation ».</p>


