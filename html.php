        	<h3>Le HTML</h3>
                    
        <p>HTML est l'acronyme d'<strong>Hypertext Murkup Language</strong> qui peut se traduire ainsi: language <br/>
		hypertexte à balise.<br/>
		Le HTML est language informatique utilisant des <strong>textes</strong> et des <strong>balises</strong>. On peut utiliser un <br/>
		simple éditeur de texte pour créer un fichier HTML(exemple,bloc-Note sur windows).<br/>
		Pour visionner un fichier sur HTML on utilise un <strong>navigateur</strong>. Un navigateur est un logiciel qui va lire <br/>
		le code HTML et l'interpreter. Chaque balise donne  au navigateur des informations<br/>
		sur comment afficher tel ou tel élément.<br/>
		Il existe plusieurs navigateur, les plus connus étant <strong>Internet Explorer</strong>(sur <br/>
		windows), <strong>Firefox</strong>(windows, Mac, Lunix) et <strong>Safari</strong>(windows, Mac). En théorie, tous ces <br/>
		navigateurs devraient afficher une meme page HTML de maniére identique. Dans la réalité, des <br/>
		diffrences subsistent.
		</p>
		
		
		

