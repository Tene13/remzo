            <h3>Le Javascript</h3>
      <p> avaScript est un langage de programmation informatique. C’est une forme de code qui permet de créer un contenu plus dynamique, plus animé ou encore de réaliser des animations complexes (images, vidéos) sur une page web.  Dans le développement web, le JavaScript permet aux pages web de disposer d’une meilleure réactivité et interactivité.

Créé en 1995 par Netscape Communication Corporation, il s’appelait à sa création le LiveScript. Après une fusion de la société Netscape avec la société SUN, le langage prend le nom de JavaScript.</p>
<p>Le langage de programmation se distingue des autres langages serveurs puisque l’exécution des tâches est opérée par le navigateur lui-même, du côté de l’utilisateur et non du serveur web. Il rejoint ainsi le rang d’ECMA Script, qui désigne les langages de scripts orientés vers le client.</p>
            <h3>Fonctionnement du javascript</h3>

           <p> Sur les pages web, le langage JavaScript permet de dicter aux pages web comment réagir lorsqu’un internaute effectue une demande.  Ainsi, la page web sera capable de comprendre les besoins du visiteur.

Le code JavaScript permet entre autres :
<ul>
<li>De faire apparaitre, de déplacer ou de supprimer des éléments sur la page
<li>De mettre en place des animations plus complexes 2D/3D
<li>De mettre à jour les éléments sans que l’internaute ait besoin de recharger la page
<li>De faire réagir la page web en fonction du comportement de l’utilisateur<
<li>De contrôler les médias, les vidéos, les images</li
<li>Sans le langage JavaScript, une page web reste statique et peu animée (malgré la présence de CSS).</li
</ul>
</p>



